package com.manju7.volley_grid;

public class Items {

    String name;
    String age;
    String img;

    public Items(String name, String age, String img) {
        this.name = name;
        this.age = age;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getImg() {
        return img;
    }
}
